readBin :: String -> Integer
readBin ('-':s) = -(readBin s)
readBin s = case readInt 2 isDigit cvt s' of
              [(a, "")] -> a
              _         -> error $ "readBin: Cannot read a binary number from: " ++ show s
  where cvt c = ord c - ord '0'
        isDigit = (`elem` "01")
        s' | "0b" `isPrefixOf` s = drop 2 s
           | otherwise                = s

firstLen :: [(Integer, Integer)]
firstLen = [(head c, genericLength c) | c <- chunks]

packets49344 :: [(Integer, Integer)]
packets49344 = [(a .&. 0b00111111111111111,0) | [49344, 2049, 0, a, 0, b] <- chunks]

packets19872 :: [(Integer, Integer, Integer)]
packets19872 = [(a, b, c) | [19872, a, b, c, 0] <- chunks]

chunksCount :: [(Integer, Integer)]
chunksCount =
  let headers = head <$> chunks
      s = sort headers
      gs = [(head g, genericLength g) | g <- group s]
  in sortOn snd gs

chunks :: [[Integer]]
chunks = dropping numbers
  where
    dropping [] = []
    dropping xs =
      let rem = dropWhile (== Nothing) xs
      in taking rem
    taking [] = []
    taking xs =
      let (chunk, rem) = break (== Nothing) xs
      in catMaybes chunk : dropping rem

numbers :: [Maybe Integer]
numbers = go <$> strings
  where
    go ('0':_)  = Nothing
    go ('1':xs) = Just (readBin xs)

